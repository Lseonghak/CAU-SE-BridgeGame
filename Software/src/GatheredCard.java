public class GatheredCard {
    private int p;
    private int h;
    private int s;
    private int bridge;

    public GatheredCard(){
        p = 0;
        h = 0;
        s = 0;
        bridge = 0;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getS() {
        return s;
    }

    public void setS(int s) {
        this.s = s;
    }

    public int getBridge() {
        return bridge;
    }

    public void setBridge(int bridge) {
        this.bridge = bridge;
    }
}
